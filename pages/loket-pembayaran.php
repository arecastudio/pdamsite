Untuk pembayaran tagihan maupun tunggakan dapat dilakukan melalui loket PDAM terdekat di wilayah pelanggan. Pembayaran dapat juga dilakukan melalui loket mitra kami dengan ketentuan khusus yakni pelanggan harus melunasi tunggakan terlebih dahulu.
<br/>
<br/>
Berikut ini adalah mitra kami yang melayani pembayaran rekening pelanggan PDAM Jayapura
<ol>
    <li>POS Indonesia (Kantor POS) - Pelanggan umum</li>
    <li>Bank Papua - Pelanggan khusus nasabah</li>
    <li>Bank BNI - Pelanggan khusus nasabah</li>
</ol>

<h4>Keterangan</h4>
<ul>
    <li><strong>Pelanggan umum</strong> adalah semua pelanggan yang terdaftar dan memiliki nomor rekening air PDAM Jayapura</li>
    <li><strong>Pelanggan khusus nasabah</strong> adalah setiap pelanggan yang terdaftar dan memiliki nomor rekening air PDAM Jayapura serta terdaftar sebagai nasabah pada Bank yang dimaksud</li>
</ul>
<hr/>
Sebelum melakukan pembayaran tagihan ataupun tunggakan pada loket resmi kami, ada baiknya pelanggan melakukan <strong><a href="<?php echo base_url();?>cek-tagihan">pengecekan tagihan secara online</a></strong> terlebih dahulu untuk mempermudah proses pembayaran di loket, dengan memberikan jumlah uang pas sesuai jumlah tagihan yang ditampilkan pada Sistem Billing kami.

