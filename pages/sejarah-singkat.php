<ol>
	<li>Tahun 1943-1944 Sistem penyediaan air minum dibangun oleh  Angkatan Darat  Amerika Serikat.</li>
	<li>Tahun 1945-1963 Pengelolaannya  diambil alih Pemerintah Kerajaan Belanda (Resident Water Staat Dienst) atau RWD.</li>
	<li>Tahun 1964-1983 Dikelola oleh Dinas Pekerjaan Umum Kabupaten  Jayapura.</li>
	<li>Tanggal 20 Agustus 1983  Melalui Surat  Keputusan Menteri PU No.128/KPTS /CK/1983 dilaksanakan pembentukan  Badan Pengelola Air Minum (BPAM) Kabupaten Jayapura.</li>
	<li>Tanggal 3 September  1984  Melalui Surat Keputusan Bupati KDH Tk II Jayapura Nomor : 188.4/97/1984 dilaksanakan pengalihan  Pengelola Air Minum  dari DPU Kabupaten Jayapura kepada BPAM yang meliputi : Jayapura Utara, Jayapura Selatan, Abepura, dengan jumlah 6.000 SR (sambungan rumah).</li>
	<li>Tanggal 20 Juni  1992, Status BPAM dialihkan  menjadi  PDAM Kabupaten Jayapura.</li>
	<li>Bulan Pebruari 2010 Penandatanganan MOU tentang Pengelolaan Bersama PDAM antara Kabupaten dan Kota Jayapura.</li>
</ol>

